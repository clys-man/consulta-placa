export function getPlateInfo(plate: string) {
  if (!isValidPlate(plate)) {
    return;
  }

  const plateLetters = plate.substring(0, 3);
  const plateInfo = rangeByState.find((item) => {
    const [start, end] = item.range.split("-");

    return plateLetters >= start && plateLetters <= end;
  });

  if (!plateInfo) {
    return;
  }

  return plateInfo;
}

function isValidPlate(plate: string) {
  const formatedPlate = plate.replace(/[^a-zA-Z0-9]/g, "").toUpperCase();
  const plateRegex = /^[A-Z]{3}[0-9]{4}$/;
  const plateIsValid = plateRegex.test(formatedPlate);

  return plateIsValid;
}
